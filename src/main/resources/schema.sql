    CONSTRAINT FK_TB_BOARD_TO_TB_REPLY_1 FOREIGN KEY (BOARD_ID) REFERENCES TB_BOARD (BOARD_ID) ON DELETE CASCADE,
    CONSTRAINT FK_TB_MEMBER_TO_TB_REPLY_2 FOREIGN KEY (MEMBER_ID) REFERENCES TB_MEMBER (MEMBER_ID)
