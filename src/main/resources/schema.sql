CREATE TABLE TB_ALARM
(
    NOTICE_ID          NUMBER                                                               NOT NULL,
    CONTENT     VARCHAR2(100)                                                        NOT NULL,
    CATEGORY           VARCHAR2(30) CHECK (CATEGORY IN ('REPLY', 'TODO', 'FOLLOW', 'LIKE')) NOT NULL,
